package datatype;

public class DtTime {
	private int horas;
	private int min;
	
	public DtTime() {
		super();
	}
	public DtTime(int horas, int min) {
		super();
		this.horas = horas;
		this.min = min;
	}
	public int getHoras() {
		return horas;
	}
	public void setHoras(int horas) {
		this.horas = horas;
	}
	public int getMin() {
		return min;
	}
	public void setMin(int min) {
		this.min = min;
	}
	
	
}
